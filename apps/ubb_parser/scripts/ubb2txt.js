// covert UBB to TXT
function ubb2txt() {
  let txt = '';
  let ubb = document.getElementById('ubb');
  let game = ubb.value.split('\n');
  
  const VARIATION = ['a', 'b', 'c', 'd', 'e', 'f', 'g', 'h', 'g', 'i', 'j'];
  var gameTree = [], moveList, variations = [], comments = [];
  var moveNumber = 1;
  
  // parse move list, variations and comments
  for (let index = 0; index < game.length; index++) {
    let ubbTag = game[index];
    
    for (let tag = 0; tag < UBB_TAGS.length; tag++) {
      let data = ubbTag.split(UBB_TAGS[tag])[1];
      let tagName = UBB_TAGS[tag].split('_')[1].split(']')[0];
      
      if (data == undefined) continue;
      data = data.split('[')[0];
      if (data == '') continue;
      
      var binit;
      
      if (tagName == 'binit') binit = data;
      else if (tagName == 'movelist') moveList = getMovelistString(data, 'international', binit).split(' ').filter(item => item.length);
      else if (tagName == 'move') {
        let moveNumber, id;
        if (data.split(']')[0].split('_').length == 3) {
          moveNumber = data.split(']')[0].split('_').map(item => parseInt(item))[1];
          id = data.split(']')[0].split('_').map(item => parseInt(item))[2];
        }
        variations.push({
          id: id,
          moveNumber: moveNumber, 
          moves: getMovelistString(data.split(']')[1], 'international', binit).split(' ').filter(item => item.length)
        });
      } else if (tagName == 'comment') comments.push({
        referer: data.split(']')[0].split('_'),
        comments: data.split(']')[1]
      });
    }
  }
  
  // map variations and comments to move list
  for (let count = 0; count < moveList.length; count++) {
    let move = moveList[count];

    gameTree.push({
      move: move,
      variations: [],
      comments: ''
    });
    
    for (let commCount = 0; commCount < comments.length; commCount++) {
      let comment = comments[commCount];
      if (comment.referer.length == 1) {
        if (parseInt(comment.referer[0]) == (count + 1)) gameTree[gameTree.length - 1].comments = comment.comments;
      } else if (comment.referer.length == 2) {
        let commId = parseInt(comment.referer[0]);
        
        for (let varCount = 0; varCount < variations.length; varCount++) {
          let variation = variations[varCount];
          if (commId == variation.id) variation.comments = comment.comments;
        }
      }
    }
    
    for (let varCount = 0; varCount < variations.length; varCount++) {
      let variation = variations[varCount];
      if (variation.moveNumber == (count + 1)) gameTree[gameTree.length - 1].variations.push({
        moves: variation.moves,
        comments: variation.comments
      });
    }
  }
  
  // convert game tree to text
  for (let item = 0; item < gameTree.length; item++) {
    let mainLineMove = gameTree[item].move;
    let mainLineComment = gameTree[item].comments;
    let variationMoves = '';
    let variationComments = '';
    
    for (let variationCount = 0; variationCount < gameTree[item].variations.length; variationCount++) {
      let variation = gameTree[item].variations[variationCount];
        
      for (let variationMoveCount = 0; variationMoveCount < variation.moves.length; variationMoveCount++) {
        let variationMove = variation.moves[variationMoveCount];
        variationMoves += variationMove + ' ';
      }
      
      variationMoves = '\n ' + VARIATION[variationCount] + '. ' + variationMoves;
      
      try {
        let variationComment = variation.comments;
        variationComments += variationComment;
      } catch(e) {}
        
      variationComments = variationComments.split('|').join('\n');
      variationMoves += variationComments ? '\n' + variationComments + '\n\n' : '';
    }
    
    let mvNum = (item % 2) ? '' : moveNumber + '. ';
    
    txt += mvNum;
    txt += mainLineMove + ' ';
    txt += mainLineComment ? '\n' + mainLineComment + '\n\n' : '';
    txt += variationMoves;
    
    (item % 2) ? '' : moveNumber++
  }
  console.log(gameTree)
  ubb.value = (comments[0].comments.length ? comments[0].comments.split('|').join('\n') + '\n\n' : '') + txt;
}


